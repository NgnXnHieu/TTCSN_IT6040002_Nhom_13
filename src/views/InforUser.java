    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        if (edit_detail_Name.getText().isEmpty() || edit_detail_SDT.getText().isEmpty()|| edit_detail_Email.getText().isEmpty() ) {
            // Nếu có trường nào rỗng, hiển thị thông báo lỗi
            JOptionPane.showMessageDialog(null, "Vui lòng điền đầy đủ thông tin!", "Lỗi", JOptionPane.ERROR_MESSAGE);
        } else if(edit_detail_SDT.getText().length()>11){
            //Kiểm tra sdt hợp lệ
            JOptionPane.showMessageDialog(null, "Số điện thoại không hợp lệ", "Lỗi", JOptionPane.ERROR_MESSAGE);
        }else {
            // Nếu tất cả các trường đều có dữ liệu
            Staff staff = new  Staff(edit_detail_Name.getText(), edit_detail_SDT.getText(), edit_detail_Email.getText(), idUser);
            sDAO.updateStaffByIdUser(staff);
            JOptionPane.showMessageDialog(null, "Sửa thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            edit_UserDetail.dispose();

            
            }

        
    }//GEN-LAST:event_jButton5ActionPerformed

