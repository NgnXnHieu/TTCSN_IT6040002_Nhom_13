    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // TODO add your handling code here:
        String old_pw= new String (pw_OldPasswordText1.getPassword());
        String npw= new String(pw_ConfirmNewPasswordText.getPassword());
        String cpw= new String(pw_ConfirmNewPasswordText.getPassword());
        if (pw_ConfirmNewPasswordText.getPassword().length==0 || pw_NewPasswordText.getPassword().length ==0|| pw_OldPasswordText1.getPassword().length==0) {
            // Nếu có trường nào rỗng, hiển thị thông báo lỗi
            JOptionPane.showMessageDialog(null, "Vui lòng điền đầy đủ mật khẩu!", "Lỗi", JOptionPane.ERROR_MESSAGE);
        }else if(!uDAO.getPasswordById(Integer.parseInt(idUser)).equals(old_pw)) {
            JOptionPane.showMessageDialog(null, "Mật khẩu không đúng", "Lỗi", JOptionPane.ERROR_MESSAGE);
        } else if(!npw.equals(cpw)) {
            JOptionPane.showMessageDialog(null, "Xác nhận mật khẩu mới không khớp", "Lỗi", JOptionPane.ERROR_MESSAGE);
        }     else {
            uDAO.updatePassword(Integer.parseInt(idUser),cpw );
            JOptionPane.showMessageDialog(null, "Đổi mật khẩu thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            edit_password.dispose();
        }
    }//GEN-LAST:event_jButton7ActionPerformed
